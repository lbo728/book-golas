export function extractKeywords(text: string, topN: number = 10): string[] {
  const words = text
    .toLowerCase()
    .replace(/[^\w\sㄱ-ㅎ가-힣]/g, "")
    .split(/\s+/)
    .filter((w) => w.length > 2 && !STOP_WORDS.has(w));

  const wordCount: Record<string, number> = {};
  words.forEach((w) => {
    wordCount[w] = (wordCount[w] || 0) + 1;
  });

  return Object.entries(wordCount)
    .sort(([, a], [, b]) => b - a)
    .slice(0, topN)
    .map(([word]) => word);
}
